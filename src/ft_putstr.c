#include "../inc/ft_printf.h"

int	ft_putstr(char *str, int *we)
{
	int	c;
	int	d;

	d = 0;
	c = 0;
	while (str[c] != '\0')
	{
		d = ft_write(1, &str[c], 1, we);
		if (d == -1)
			return (-1);
		c++;
	}
	return (0);
}
